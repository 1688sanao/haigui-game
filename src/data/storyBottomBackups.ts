import type { StoryDifficulty } from "./stories";

function buildBackups(partsA: string[], partsB: string[], partsC: string[]): string[] {
  const list: string[] = [];
  for (const a of partsA) {
    for (const b of partsB) {
      for (const c of partsC) {
        list.push(`${a}${b}${c}`);
      }
    }
  }
  return list.slice(0, 100);
}

